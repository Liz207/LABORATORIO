    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        //-- Guardar asume guardar nuevo y modificar

        //-- Creamos una copia del objeto y le llenamos los datos con el formulario
        Vuelos miVuelo = new Vuelos();
        miVuelo.setVueloID(Integer.parseInt(txtIDVuelo.getText()));
        miVuelo.setNumeroVuelo(txtNumeroVuelo.getText());
        miVuelo.setAeropuertoOrigenID(Integer.parseInt(txtFkAeroOrigen.getText()));
        miVuelo.setAeropuertoDestinoID(Integer.parseInt(txtFkAeroDes.getText()));
        miVuelo.setFechaSalida(new java.sql.Date(jdcFechaSalida.getDate().getTime()));
        int indice = 0;

        try {
            Vuelos Consultada = Conexion.obtenerVuelo(txtIDVuelo.getText() );
            if (Consultada != null){
                indice = Consultada.getVueloID();
            }
            else {
                indice = 0;
            }
        } catch (SQLException ex) {
            indice = 0;
        }

        //---Insertar o Modificar en la tabla
        if (indice == 0){
            try {

                if (Conexion.insetVuelo(miVuelo) != 0) {
                    JOptionPane.showMessageDialog(null, "Vuelo Guardado" );
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.toString() );
            }
        }else{
            try {
                if (Conexion.updateVuelo(miVuelo) != 0) {
                    JOptionPane.showMessageDialog(null, "Vuelo Modificado" );
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.toString() );
            }

        }
        btnCosultar.doClick();
        limpiar();
    }//GEN-LAST:event_btnGuardarActionPerformed

