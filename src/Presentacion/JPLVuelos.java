    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String ID = "";
        if (  ID.equals(txtIDVuelo.getText() ) ){
            JOptionPane.showMessageDialog(null,
                "Debe digitar algo en el campo de Identificador",
                "Mensaje Importante",
                JOptionPane.ERROR_MESSAGE);
        } else{
            try{
                Vuelos miVuelo = Conexion.obtenerVuelo(txtIDVuelo.getText() );
                txtNumeroVuelo.setText(miVuelo.getNumeroVuelo());
                txtNomAeroOrigen.setText(String.valueOf(miVuelo.getAeropuertoOrigenID()));
                txtNomAeroDes.setText(String.valueOf(miVuelo.getAeropuertoDestinoID()));
                jdcFechaSalida.setDate(new java.util.Date(miVuelo.getFechaSalida().getTime()));
            }catch (SQLException ex){
                Logger.getLogger(JPLAeropuertos.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

